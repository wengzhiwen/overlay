export const metersPerSecondToKilometersPerHour = (value: number): number => {
  return value * 3.6;
};

export const metersToKilometers = (value: number): number => {
  return value / 1000;
};

export const metersPerSecondToMilesPerHour = (value: number): number => {
  return value * 2.2369362921;
};

export const metersToMiles = (value: number): number => {
  return value / 1609.344;
};

export const metersToFeet = (value: number): number => {
  return value * 3.280839895;
};
